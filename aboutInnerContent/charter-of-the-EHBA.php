            <div class="charterWrapper__holder__block--content">
                <ol class="listHolder">
                    <li class="listHolder--item">European Hardcourt Association (EHA)
                        <ul>
                            <li>The European Hardcourt Association is the highest bike polo institution in Europe; therefore, it has authority on all countries that presents.</li>
                            <li>Even if EHA has authority on national committees, EHA would let them act freely, as long as it doesn’t interfere with this charter.</li>
                        </ul>
                    </li>
                    <li class="listHolder--item">European countries
                        <ul>
                            <li>Is considered an European country for EHA:
                                <ul>
                                    <li>All countries that belongs in the geographic Europe</li>
                                    <li>All island that are usually grouped with Europe (such as United Kingdom, Ireland, Iceland etc.)</li>
                                    <li>And transcontinental [1] states belonging to Europe Territory and Asian Territory (Azerbaijan,Georgia, Eastern Russia and Turkey).</li>
                                </ul>
                            </li>
                            <li>Exceptions can be made if representatives of a country ask for temporary inclusion of their bike polo scene in Europena Hardcourt. Not a Committee representing their own geographic region, and are considered close enough to participate to EHBPC. Those countries will not have the right to organise EHBPC.</li>
                        </ul>
                    </li>
                </ol>
            </div>
